package LaChiqui;

public class Convidado {

    public String nome;
    public String grito;


    public Convidado(String nome) {}

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getGrito() {
        return grito;
    }

}
